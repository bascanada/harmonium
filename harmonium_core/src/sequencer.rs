// --- RHYTHM MODE (Strategy Pattern) ---

#[derive(Clone, Copy, Debug, PartialEq, Eq, Default, Serialize, Deserialize)]
pub enum RhythmMode {
    #[default]
    Euclidean, // Algorithme de Bjorklund (Classique)
    PerfectBalance, // Algorithme Additif (XronoMorph style) - Polygones réguliers
    ClassicGroove,  // Patterns de batterie réalistes (ghost notes, grooves)
}

/// Événement déclenché à chaque step du séquenceur
/// Indique quelles voix doivent jouer
#[derive(Clone, Copy, Debug, Default, PartialEq)]
pub struct StepTrigger {
    pub kick: bool,    // Fondation (Square/Octagon)
    pub snare: bool,   // Tension (Triangle/Backbeat)
    pub hat: bool,     // Remplissage (Euclidean Fills)
    pub bass: bool,    // Harmonie Basse (Channel 0)
    pub lead: bool,    // Mélodie (Channel 1)
    pub velocity: f32, // Dynamique générale (0.0 à 1.0)
}

impl StepTrigger {
    #[must_use]
    pub const fn is_any(&self) -> bool {
        self.kick || self.snare || self.hat || self.bass || self.lead
    }
}

// --- POLYGON (Perfect Balance Primitive) ---

/// Maximum number of positions in an irregular polygon or accent pattern.
pub const MAX_POLYGON_POSITIONS: usize = 16;

/// Maximum number of entries in a per-vertex accent pattern.
pub const MAX_ACCENT_LEN: usize = MAX_POLYGON_POSITIONS;

/// A fixed-size accent pattern that maintains `Copy`.
/// Stores up to [`MAX_ACCENT_LEN`] velocity multipliers applied cyclically to vertices.
#[derive(Clone, Copy, Debug, Serialize, Deserialize)]
pub struct AccentPattern {
    /// Buffer of velocity multipliers (only the first `len` entries are meaningful).
    buf: [f32; MAX_ACCENT_LEN],
    /// Number of active entries (0 = uniform velocity, no per-vertex modulation).
    len: usize,
}

impl AccentPattern {
    /// An empty accent pattern (uniform velocity).
    #[must_use]
    pub const fn empty() -> Self {
        Self { buf: [1.0; MAX_ACCENT_LEN], len: 0 }
    }

    /// Create an accent pattern from a slice. Panics if `slice.len() > MAX_ACCENT_LEN`.
    #[must_use]
    pub fn from_slice(slice: &[f32]) -> Self {
        assert!(
            slice.len() <= MAX_ACCENT_LEN,
            "accent pattern length {} exceeds MAX_ACCENT_LEN ({})",
            slice.len(),
            MAX_ACCENT_LEN
        );
        let mut buf = [1.0; MAX_ACCENT_LEN];
        buf[..slice.len()].copy_from_slice(slice);
        Self { buf, len: slice.len() }
    }

    /// Returns `true` when the pattern is empty (uniform velocity).
    #[must_use]
    pub const fn is_empty(&self) -> bool {
        self.len == 0
    }

    /// Number of active entries.
    #[must_use]
    pub const fn len(&self) -> usize {
        self.len
    }

    /// Returns the multiplier for the given vertex index (wraps cyclically).
    #[must_use]
    pub const fn get(&self, vertex_index: usize) -> f32 {
        if self.len == 0 {
            1.0
        } else {
            self.buf[vertex_index % self.len]
        }
    }
}

impl Default for AccentPattern {
    fn default() -> Self {
        Self::empty()
    }
}

impl PartialEq for AccentPattern {
    fn eq(&self, other: &Self) -> bool {
        if self.len != other.len {
            return false;
        }
        self.buf[..self.len] == other.buf[..other.len]
    }
}

/// Describes whether a polygon's vertices are evenly spaced or explicitly placed.
#[derive(Clone, Copy, Debug, Default, PartialEq, Serialize, Deserialize)]
pub enum PolygonShape {
    /// Vertices evenly spaced around the circle (current default behavior).
    #[default]
    Regular,
    /// Explicit onset positions within the cycle (fixed array for Copy).
    Irregular {
        positions: [usize; MAX_POLYGON_POSITIONS],
        count: usize,
    },
}

/// Represents a regular polygon inscribed in the time cycle.
/// This is the atomic unit of the "Perfect Balance" algorithm.
#[derive(Clone, Copy, Debug, Serialize, Deserialize)]
pub struct Polygon {
    /// Number of vertices (e.g., 3 = Triangle, 4 = Square).
    /// Ideally should be a divisor of the total steps for perfect alignment.
    pub vertices: usize,

    /// Rotation in steps (Phase shift).
    /// Determines where the polygon starts on the circle.
    pub rotation_offset: usize,

    /// Velocity (Amplitude) of events generated by this polygon.
    /// Negative velocity suppresses onsets from other layers.
    pub velocity: f32,

    /// Shape of this polygon (regular or irregular vertex placement).
    #[serde(default)]
    pub shape: PolygonShape,

    /// Per-vertex accent pattern (velocity multipliers applied cyclically).
    pub accent_pattern: AccentPattern,
}

impl Polygon {
    /// Standard constructor (regular polygon, uniform velocity, no per-vertex accents).
    #[must_use]
    pub const fn new(vertices: usize, rotation_offset: usize, velocity: f32) -> Self {
        Self {
            vertices,
            rotation_offset,
            velocity,
            shape: PolygonShape::Regular,
            accent_pattern: AccentPattern::empty(),
        }
    }

    /// Constructor with a per-vertex accent pattern (regular polygon).
    #[must_use]
    pub fn with_accents(
        vertices: usize,
        rotation_offset: usize,
        velocity: f32,
        accent_pattern: AccentPattern,
    ) -> Self {
        Self { vertices, rotation_offset, velocity, shape: PolygonShape::Regular, accent_pattern }
    }

    /// Constructor for an irregular polygon with explicit onset positions.
    #[must_use]
    pub fn irregular(onset_positions: &[usize], velocity: f32, accent: AccentPattern) -> Self {
        let count = onset_positions.len().min(MAX_POLYGON_POSITIONS);
        let mut positions = [0usize; MAX_POLYGON_POSITIONS];
        positions[..count].copy_from_slice(&onset_positions[..count]);
        Self {
            vertices: count,
            rotation_offset: 0,
            velocity,
            shape: PolygonShape::Irregular { positions, count },
            accent_pattern: accent,
        }
    }

    /// Returns the effective velocity for a given vertex index,
    /// applying the accent pattern multiplier if present.
    #[must_use]
    pub fn velocity_at(&self, vertex_index: usize) -> f32 {
        self.velocity * self.accent_pattern.get(vertex_index)
    }

    /// Checks if this polygon has an active vertex at the given step.
    /// For irregular polygons, checks explicit positions.
    /// For regular polygons, uses a fast path when `total_steps` is divisible
    /// by vertices, and falls back to a Euclidean rhythm algorithm otherwise.
    #[must_use]
    pub fn hits(&self, step: usize, total_steps: usize) -> bool {
        match self.shape {
            PolygonShape::Irregular { positions, count } => {
                for i in 0..count {
                    if positions[i] == step {
                        return true;
                    }
                }
                false
            }
            PolygonShape::Regular => self.hits_regular(step, total_steps),
        }
    }

    /// Returns `(is_hit, vertex_index)` for the given step.
    /// For irregular polygons, the vertex index is the position in the onset array.
    /// For regular polygons, computes which vertex number this step corresponds to.
    #[must_use]
    pub fn hit_info(&self, step: usize, total_steps: usize) -> (bool, usize) {
        match self.shape {
            PolygonShape::Irregular { positions, count } => {
                for i in 0..count {
                    if positions[i] == step {
                        return (true, i);
                    }
                }
                (false, 0)
            }
            PolygonShape::Regular => {
                if !self.hits_regular(step, total_steps) {
                    return (false, 0);
                }
                let hit_idx = self.regular_hit_index(step, total_steps);
                (true, hit_idx)
            }
        }
    }

    /// Regular polygon hit test (original algorithm).
    fn hits_regular(&self, step: usize, total_steps: usize) -> bool {
        if self.vertices == 0 || total_steps == 0 {
            return false;
        }
        if self.vertices > total_steps {
            return false;
        }

        let adjusted_step =
            (step + total_steps - (self.rotation_offset % total_steps)) % total_steps;

        if total_steps.is_multiple_of(self.vertices) {
            let interval = total_steps / self.vertices;
            adjusted_step.is_multiple_of(interval)
        } else {
            let prev_adjusted_step = (adjusted_step + total_steps - 1) % total_steps;
            (adjusted_step * self.vertices) / total_steps
                != (prev_adjusted_step * self.vertices) / total_steps
        }
    }

    /// Compute the ordinal hit index for a regular polygon at a given step.
    fn regular_hit_index(&self, step: usize, total_steps: usize) -> usize {
        if self.vertices == 0 || total_steps == 0 {
            return 0;
        }
        let adjusted_step =
            (step + total_steps - (self.rotation_offset % total_steps)) % total_steps;
        if total_steps.is_multiple_of(self.vertices) {
            let interval = total_steps / self.vertices;
            if interval == 0 { 0 } else { adjusted_step / interval }
        } else {
            (adjusted_step * self.vertices) / total_steps
        }
    }
}

/// Configuration for an extra polygon layer (used in `PerfectBalanceParams`).
#[derive(Clone, Copy, Debug, PartialEq, Serialize, Deserialize)]
pub struct PolygonConfig {
    /// Number of vertices (for regular shapes).
    pub vertices: usize,
    /// Rotation offset in steps.
    pub rotation_offset: usize,
    /// Base velocity (negative = suppression polygon).
    pub velocity: f32,
    /// Accent pattern applied cyclically to hits.
    #[serde(default)]
    pub accent_pattern: AccentPattern,
    /// Shape of this polygon.
    #[serde(default)]
    pub shape: PolygonShape,
}

impl PolygonConfig {
    /// Convert this config into a `Polygon`.
    #[must_use]
    pub fn to_polygon(&self) -> Polygon {
        Polygon {
            vertices: self.vertices,
            rotation_offset: self.rotation_offset,
            velocity: self.velocity,
            shape: self.shape,
            accent_pattern: self.accent_pattern,
        }
    }
}

// --- SEQUENCER STRUCT ---

pub struct Sequencer {
    pub time_signature: TimeSignature,
    pub ticks_per_beat: usize,
    pub steps: usize,
    pub pulses: usize,
    pub pattern: Vec<StepTrigger>, // Currently playing buffer
    pub next_pattern: Option<Vec<StepTrigger>>, // Prepared buffer for next bar
    pub rotation: usize,
    pub current_step: usize,
    pub bpm: f32,
    pub last_tick_time: f64,
    pub mode: RhythmMode,
    pub tension: f32,
    pub density: f32,
    /// Target rhythmic oddity score (4-7+) for pattern generation
    /// Used by UnifiedTensionSystem to couple rhythmic and harmonic tension
    pub target_oddity: Option<usize>,
    /// PerfectBalance rhythm tuning.
    pub pb_params: PerfectBalanceParams,
    /// ClassicGroove rhythm tuning.
    pub cg_params: ClassicGrooveParams,
}

/// Résultat d'un tick du séquenceur
#[derive(Clone, Copy, Debug, Default)]
pub struct TickResult {
    pub trigger: StepTrigger,
    pub bar_crossed: bool,
}

impl Sequencer {
    #[must_use]
    pub fn new(steps: usize, pulses: usize, bpm: f32) -> Self {
        Self::new_with_mode(steps, pulses, bpm, RhythmMode::Euclidean)
    }

    #[must_use]
    pub fn new_with_mode(steps: usize, pulses: usize, bpm: f32, mode: RhythmMode) -> Self {
        let mut seq = Self {
            time_signature: TimeSignature::default(),
            ticks_per_beat: 4,
            steps,
            pulses,
            pattern: vec![StepTrigger::default(); steps],
            next_pattern: None,
            rotation: 0,
            current_step: 0,
            bpm,
            last_tick_time: 0.0,
            mode,
            tension: 0.0,
            density: 0.5,
            target_oddity: None,
            pb_params: PerfectBalanceParams::default(),
            cg_params: ClassicGrooveParams::default(),
        };
        seq.regenerate_pattern();
        seq
    }

    #[must_use]
    pub fn new_with_rotation(steps: usize, pulses: usize, bpm: f32, rotation: usize) -> Self {
        let mut seq = Self::new(steps, pulses, bpm);
        seq.set_rotation(rotation);
        seq
    }

    pub fn set_rotation(&mut self, offset: usize) {
        self.rotation = offset % self.steps;
        // On régénère simplement pour appliquer la rotation
        // (Optimisation possible : rotation in-place, mais régénérer est plus sûr ici)
        self.regenerate_pattern();
    }

    pub fn regenerate_pattern(&mut self) {
        let mut raw = match self.mode {
            RhythmMode::Euclidean => {
                // Mode classique : Constrain to bar length and add meter-aware snare
                let bar_steps = self.time_signature.steps_per_bar(self.ticks_per_beat);
                let bools = generate_euclidean_bools(bar_steps, self.pulses);

                // Calculate beat positions for snare placement
                let beats_in_bar = self.time_signature.numerator;
                let beat = bar_steps / beats_in_bar;

                let mut pattern: Vec<StepTrigger> = bools
                    .into_iter()
                    .enumerate()
                    .map(|(i, b)| StepTrigger {
                        kick: b,
                        snare: false, // Will be set below
                        hat: b,       // Layering simple
                        bass: b,      // Sync bass with kick in Euclidean mode
                        lead: b,
                        velocity: if i == 0 {
                            1.0
                        } else if b {
                            0.85
                        } else {
                            0.0
                        },
                    })
                    .collect();

                // Add snare on backbeats based on meter
                if beats_in_bar == 4 {
                    // 4/4: snare on beats 2 and 4
                    if beat < pattern.len() {
                        pattern[beat].snare = true;
                        pattern[beat].velocity = 1.0;
                    }
                    if 3 * beat < pattern.len() {
                        pattern[3 * beat].snare = true;
                        pattern[3 * beat].velocity = 1.0;
                    }
                } else if beats_in_bar == 3 {
                    // 3/4: snare on beat 3
                    if 2 * beat < pattern.len() {
                        pattern[2 * beat].snare = true;
                        pattern[2 * beat].velocity = 1.0;
                    }
                } else if beats_in_bar == 2 {
                    // 2/4: snare on beat 2
                    if beat < pattern.len() {
                        pattern[beat].snare = true;
                        pattern[beat].velocity = 1.0;
                    }
                } else {
                    // Odd meters: snare on last beat
                    let pos = (beats_in_bar - 1) * beat;
                    if pos < pattern.len() {
                        pattern[pos].snare = true;
                        pattern[pos].velocity = 1.0;
                    }
                }

                pattern
            }
            RhythmMode::PerfectBalance => generate_balanced_layers(
                self.density,
                self.tension,
                self.time_signature,
                self.ticks_per_beat,
                &self.pb_params,
            ),
            RhythmMode::ClassicGroove => generate_classic_groove(
                self.steps,
                self.density,
                self.tension,
                self.time_signature,
                &self.cg_params,
            ),
        };

        // Appliquer la rotation (safe: handle edge cases)
        let raw_len = raw.len();
        if self.rotation > 0 && raw_len > 0 {
            let safe_rotation = self.rotation % raw_len;
            if safe_rotation > 0 {
                raw.rotate_left(raw_len - safe_rotation);
            }
        }

        self.pattern = raw;
    }

    /// Prepare the next bar in the look-ahead buffer.
    pub fn prepare_next_bar(&mut self) {
        let mut raw = match self.mode {
            RhythmMode::Euclidean => {
                // Mode classique : Constrain to bar length and add meter-aware snare
                let bar_steps = self.time_signature.steps_per_bar(self.ticks_per_beat);
                let bools = generate_euclidean_bools(bar_steps, self.pulses);

                // Calculate beat positions for snare placement
                let beats_in_bar = self.time_signature.numerator;
                let beat = bar_steps / beats_in_bar;

                let mut pattern: Vec<StepTrigger> = bools
                    .into_iter()
                    .enumerate()
                    .map(|(i, b)| StepTrigger {
                        kick: b,
                        snare: false, // Will be set below
                        hat: b,       // Layering simple
                        bass: b,      // Sync bass with kick in Euclidean mode
                        lead: b,
                        velocity: if i == 0 {
                            1.0
                        } else if b {
                            0.85
                        } else {
                            0.0
                        },
                    })
                    .collect();

                // Add snare on backbeats based on meter
                if beats_in_bar == 4 {
                    // 4/4: snare on beats 2 and 4
                    if beat < pattern.len() {
                        pattern[beat].snare = true;
                        pattern[beat].velocity = 1.0;
                    }
                    if 3 * beat < pattern.len() {
                        pattern[3 * beat].snare = true;
                        pattern[3 * beat].velocity = 1.0;
                    }
                } else if beats_in_bar == 3 {
                    // 3/4: snare on beat 3
                    if 2 * beat < pattern.len() {
                        pattern[2 * beat].snare = true;
                        pattern[2 * beat].velocity = 1.0;
                    }
                } else if beats_in_bar == 2 {
                    // 2/4: snare on beat 2
                    if beat < pattern.len() {
                        pattern[beat].snare = true;
                        pattern[beat].velocity = 1.0;
                    }
                } else {
                    // Odd meters: snare on last beat
                    let pos = (beats_in_bar - 1) * beat;
                    if pos < pattern.len() {
                        pattern[pos].snare = true;
                        pattern[pos].velocity = 1.0;
                    }
                }

                pattern
            }
            RhythmMode::PerfectBalance => generate_balanced_layers(
                self.density,
                self.tension,
                self.time_signature,
                self.ticks_per_beat,
                &self.pb_params,
            ),
            RhythmMode::ClassicGroove => generate_classic_groove(
                self.steps,
                self.density,
                self.tension,
                self.time_signature,
                &self.cg_params,
            ),
        };

        // Apply rotation
        let raw_len = raw.len();
        if self.rotation > 0 && raw_len > 0 {
            let safe_rotation = self.rotation % raw_len;
            if safe_rotation > 0 {
                raw.rotate_left(raw_len - safe_rotation);
            }
        }

        self.next_pattern = Some(raw);
    }

    /// Upgrade to a specific number of steps (48, 96, etc.)
    pub fn upgrade_to_steps(&mut self, new_steps: usize) {
        if self.steps != new_steps && new_steps > 0 {
            self.steps = new_steps;
            self.current_step = 0;
            // Clamp rotation to new step count to prevent overflow in regenerate_pattern
            self.rotation %= new_steps;
            self.pattern = vec![StepTrigger::default(); new_steps];
            self.regenerate_pattern();
        }
    }

    /// Legacy: upgrade to 48 steps
    pub fn upgrade_to_48_steps(&mut self) {
        self.upgrade_to_steps(48);
    }

    pub fn tick(&mut self) -> TickResult {
        if self.pattern.is_empty() {
            return TickResult::default();
        }
        // Safety: clamp current_step to pattern bounds (can become invalid if steps changed)
        if self.current_step >= self.pattern.len() {
            self.current_step = 0;
        }
        let trigger = self.pattern[self.current_step];
        self.current_step += 1;

        let mut bar_crossed = false;
        if self.current_step >= self.pattern.len() {
            self.current_step = 0;
            bar_crossed = true;

            // Look-Ahead Buffer Swap
            if let Some(next) = self.next_pattern.take() {
                self.pattern = next;
                self.steps = self.pattern.len();
            }
        }

        TickResult { trigger, bar_crossed }
    }
}

/// Génère les booléens de Bjorklund (Legacy)
#[must_use]
pub fn generate_euclidean_bools(steps: usize, pulses: usize) -> Vec<bool> {
    if pulses == 0 {
        return vec![false; steps];
    }
    if pulses >= steps {
        return vec![true; steps];
    }

    let mut pattern: Vec<Vec<u8>> = Vec::new();
    for _ in 0..pulses {
        pattern.push(vec![1]);
    }
    for _ in 0..(steps - pulses) {
        pattern.push(vec![0]);
    }

    let mut count = std::cmp::min(pulses, steps - pulses);
    let mut remainder = pattern.len() - count;

    while remainder > 1 && count > 0 {
        for i in 0..count {
            if let Some(last) = pattern.pop() {
                pattern[i].extend(last);
            }
        }
        remainder = pattern.len() - count;
        count = std::cmp::min(count, remainder);
    }

    let mut result = Vec::new();
    for group in pattern {
        for val in group {
            result.push(val == 1);
        }
    }
    result
}

/// Returns vertex counts that divide `total_steps` evenly,
/// filtered to the musically useful range [2, total_steps/2].
/// These produce beat-aligned lead triggers with clean note durations
/// (quarter, eighth, half) — no dotted-eighth clutter.
fn notation_safe_vertices(total_steps: usize) -> Vec<usize> {
    (2..=total_steps / 2).filter(|d| total_steps % d == 0).collect()
}

// --- ALGORITHME PERFECT BALANCE / WELL-FORMED ---
// Based on XronoMorph theory: superposition of regular polygons
// Density controls polygon complexity (number of vertices)
// Tension controls phase shift (rotation) for syncopation

/// Generates a pattern based on the superposition of regular polygons.
/// Respects the Perfect Balance theorem (sum of vectors is zero).
/// Now meter-aware: respects time signature for bar length and snare placement.
#[must_use]
pub fn generate_balanced_layers(
    density: f32,
    tension: f32,
    ts: TimeSignature,
    ticks_per_beat: usize,
    pb: &PerfectBalanceParams,
) -> Vec<StepTrigger> {
    // Calculate bar length from time signature
    let steps = ts.steps_per_bar(ticks_per_beat);
    let mut triggers = vec![StepTrigger::default(); steps];

    // Calculate beat positions for meter-aware snare
    let beats_in_bar = ts.numerator;
    let beat = steps / beats_in_bar;

    // --- 1. LAYER CONFIGURATION ---

    // LAYER A: KICK (Foundation)
    // Uses stable (even) shapes aligned with bar structure
    let kick_gon = if density < pb.kick_polygon_low_threshold {
        Polygon::with_accents(2, 0, pb.kick_low_velocity, pb.kick_accent_pattern)
    } else {
        let kick_vertices = if beats_in_bar == 4 { 4 } else { beats_in_bar.min(4) };
        Polygon::with_accents(kick_vertices, 0, pb.kick_normal_velocity, pb.kick_accent_pattern)
    };

    // LAYER B: SNARE (Counterpoint) - METER-AWARE
    // Place snare on musically appropriate backbeats
    let snare_gon = if beats_in_bar == 4 {
        Polygon::with_accents(2, beat, pb.snare_velocity, pb.snare_accent_pattern)
    } else if beats_in_bar == 3 {
        Polygon::with_accents(1, 2 * beat, pb.snare_velocity, pb.snare_accent_pattern)
    } else if beats_in_bar == 5 {
        Polygon::with_accents(1, 2 * beat, pb.snare_velocity, pb.snare_accent_pattern)
    } else if beats_in_bar == 2 {
        Polygon::with_accents(1, beat, pb.snare_velocity, pb.snare_accent_pattern)
    } else {
        Polygon::with_accents(
            1,
            (beats_in_bar - 1) * beat,
            pb.snare_velocity,
            pb.snare_accent_pattern,
        )
    };

    // LAYER C: HI-HAT (Fill)
    // High frequency shapes.
    let hat_vertices = if density < pb.hat_density_thresholds[0] {
        pb.hat_vertex_counts[0]
    } else if density < pb.hat_density_thresholds[1] {
        pb.hat_vertex_counts[1]
    } else if density < pb.hat_density_thresholds[2] {
        pb.hat_vertex_counts[2]
    } else {
        pb.hat_vertex_counts[3]
    };

    // The Hat often "floats" around the beat to create groove.
    // Use tension to slightly shift the Hat (Mathematical Swing).
    #[allow(clippy::cast_possible_truncation, clippy::cast_sign_loss)]
    let hat_offset = if tension > pb.swing_tension_threshold {
        (tension * (steps / hat_vertices) as f32 * pb.swing_scaling_factor) as usize
    } else {
        0
    };

    let hat_gon = Polygon::with_accents(
        hat_vertices,
        hat_offset,
        pb.hat_velocity_coefficient * density.max(0.5),
        pb.hat_accent_pattern,
    );

    // LAYER D: BASS (Harmonic Foundation)
    let bass_gon = if density < pb.bass_low_density_threshold {
        // Copy kick polygon but apply bass accent pattern
        let mut bass = kick_gon;
        bass.accent_pattern = pb.bass_accent_pattern;
        bass
    } else {
        Polygon::with_accents(8, 0, pb.bass_polygon_velocity, pb.bass_accent_pattern)
    };

    // LAYER E: LEAD (Melody)
    // Use only vertex counts that divide `steps` evenly so triggers land on
    // beat/sub-beat boundaries → clean note durations (quarter, eighth, half).
    let divisors = notation_safe_vertices(steps);
    #[allow(clippy::cast_possible_truncation, clippy::cast_sign_loss)]
    let lead_vertices = if divisors.is_empty() {
        2
    } else {
        let idx = (density * (divisors.len() - 1) as f32).round() as usize;
        divisors[idx.min(divisors.len() - 1)]
    };
    // Quantize offset to half-beat boundaries so tension shifts stay beat-aligned.
    let interval = steps / lead_vertices;
    let half_beat = ticks_per_beat / 2;
    #[allow(clippy::cast_possible_truncation, clippy::cast_sign_loss)]
    let raw_offset = (tension * interval as f32) as usize;
    let lead_offset = if half_beat > 0 { (raw_offset / half_beat) * half_beat } else { raw_offset };
    let lead_gon =
        Polygon::with_accents(lead_vertices, lead_offset, pb.lead_polygon_velocity, pb.lead_accent_pattern);

    // --- 2. RASTERIZATION (Collision Calculation) ---
    // Build polygon lists per instrument: primary + extras.
    let kick_polygons: Vec<Polygon> = std::iter::once(kick_gon)
        .chain(pb.kick_extra_polygons.iter().map(PolygonConfig::to_polygon))
        .collect();
    let snare_polygons: Vec<Polygon> = std::iter::once(snare_gon)
        .chain(pb.snare_extra_polygons.iter().map(PolygonConfig::to_polygon))
        .collect();
    let hat_polygons: Vec<Polygon> = std::iter::once(hat_gon)
        .chain(pb.hat_extra_polygons.iter().map(PolygonConfig::to_polygon))
        .collect();
    let bass_polygons: Vec<Polygon> = std::iter::once(bass_gon)
        .chain(pb.bass_extra_polygons.iter().map(PolygonConfig::to_polygon))
        .collect();
    let lead_polygons: Vec<Polygon> = std::iter::once(lead_gon)
        .chain(pb.lead_extra_polygons.iter().map(PolygonConfig::to_polygon))
        .collect();

    /// Compute the merged velocity for a set of polygons at a given step.
    /// Positive velocities are max-merged; negative velocities subtract.
    /// Returns `None` if no polygon hits this step.
    fn merged_velocity(polygons: &[Polygon], step: usize, total_steps: usize) -> Option<f32> {
        let mut vel: f32 = 0.0;
        let mut any_hit = false;
        for poly in polygons {
            let (hit, hit_idx) = poly.hit_info(step, total_steps);
            if hit {
                let v = poly.velocity_at(hit_idx);
                if v < 0.0 {
                    // Negative velocity suppresses
                    vel += v;
                } else {
                    // Positive: take max of all positive contributions
                    vel = if any_hit { vel.max(v) } else { v };
                }
                any_hit = true;
            }
        }
        if any_hit { Some(vel.max(0.0)) } else { None }
    }

    for (i, trigger) in triggers.iter_mut().enumerate().take(steps) {
        let kick_vel = merged_velocity(&kick_polygons, i, steps);
        let snare_vel = merged_velocity(&snare_polygons, i, steps);
        let hat_vel = merged_velocity(&hat_polygons, i, steps);
        let bass_vel = merged_velocity(&bass_polygons, i, steps);
        let lead_vel = merged_velocity(&lead_polygons, i, steps);

        let hit_kick = kick_vel.is_some_and(|v| v > 0.0);
        let hit_snare = snare_vel.is_some_and(|v| v > 0.0);

        // --- 3. CONFLICT RESOLUTION & VELOCITY ---

        if hit_kick {
            trigger.kick = true;
            trigger.velocity = kick_vel.unwrap_or(0.0);
        }

        if hit_snare {
            trigger.snare = true;
            // If snare hits same time as kick, accentuate it
            trigger.velocity = if hit_kick { 1.0 } else { snare_vel.unwrap_or(0.0) };
        }

        if let Some(hv) = hat_vel {
            if hv > 0.0 {
                // Masking logic: Don't necessarily play HH if Kick or Snare plays
                // Unless density is very high (Wall of sound)
                let mask = (hit_kick || hit_snare) && density < pb.hat_masking_density_threshold;

                if !mask {
                    trigger.hat = true;
                    // If it's the only element, apply its velocity
                    if !hit_kick && !hit_snare {
                        trigger.velocity = hv;
                    }
                }
            }
        }

        if bass_vel.is_some_and(|v| v > 0.0) {
            trigger.bass = true;
        }
        if lead_vel.is_some_and(|v| v > 0.0) {
            trigger.lead = true;
        }

        // --- 4. BEAT 1 ACCENT (Meter-aware) ---
        // Ensure beat 1 (downbeat) has maximum velocity
        if i == 0 && trigger.is_any() {
            trigger.velocity = 1.0;
        }
    }

    triggers
}

// --- ALGORITHME CLASSIC GROOVE ---
// Basé sur de vrais patterns de batterie musicaux
// Density contrôle la complexité rythmique
// Tension contrôle la syncopation et les ghost notes

/// Generates realistic drum patterns based on common grooves.
/// Uses density for kick complexity and tension for ghost notes/syncopation.
#[allow(clippy::too_many_lines)]
#[must_use]
pub fn generate_classic_groove(
    steps: usize,
    density: f32,
    tension: f32,
    ts: TimeSignature,
    cg: &ClassicGrooveParams,
) -> Vec<StepTrigger> {
    let mut pattern = vec![StepTrigger::default(); steps];

    // === SUBDIVISIONS ===
    let beats_in_bar = ts.numerator;
    let beat = steps / beats_in_bar; // Ticks par temps
    let eighth = beat / 2; // Croche
    let sixteenth = beat / 4; // Double-croche

    // === KICK PATTERNS ===
    // Basés sur des grooves réels de batterie
    let kdt = &cg.kick_density_thresholds;
    match density {
        d if d < kdt[0] => {
            // Half-time: Kick sur 1 seulement
            pattern[0].kick = true;
            pattern[0].velocity = cg.kick_downbeat_velocity;
        }
        d if d < kdt[1] => {
            // Half-time avec accent secondaire
            if beats_in_bar >= 4 {
                for &pos in &[0, 2 * beat] {
                    pattern[pos].kick = true;
                    pattern[pos].velocity = cg.kick_downbeat_velocity;
                }
            } else {
                pattern[0].kick = true;
                pattern[0].velocity = cg.kick_downbeat_velocity;
            }
        }
        d if d < kdt[2] => {
            // Straight beats (Pulse on every beat)
            for i in 0..beats_in_bar {
                pattern[i * beat].kick = true;
                pattern[i * beat].velocity =
                    if i == 0 { cg.kick_downbeat_velocity } else { cg.kick_secondary_velocity };
            }
        }
        d if d < kdt[3] => {
            // Groove avec anticipation
            pattern[0].kick = true;
            pattern[0].velocity = cg.kick_downbeat_velocity;
            if beats_in_bar >= 4 {
                pattern[beat + eighth].kick = true;
                pattern[beat + eighth].velocity = cg.kick_anticipation_velocity;
                pattern[2 * beat].kick = true;
                pattern[2 * beat].velocity = 0.9;
                pattern[3 * beat].kick = true;
                pattern[3 * beat].velocity = 0.8;
            } else if beats_in_bar == 3 {
                pattern[beat + eighth].kick = true;
                pattern[beat + eighth].velocity = cg.kick_anticipation_velocity;
                pattern[2 * beat].kick = true;
                pattern[2 * beat].velocity = 0.9;
            }
        }
        _ => {
            // Breakbeat style
            pattern[0].kick = true;
            pattern[0].velocity = cg.kick_downbeat_velocity;
            if beats_in_bar >= 4 {
                pattern[beat + eighth].kick = true;
                pattern[beat + eighth].velocity = 0.75;
                if sixteenth > 0 {
                    pattern[2 * beat + sixteenth].kick = true;
                    pattern[2 * beat + sixteenth].velocity = cg.kick_anticipation_velocity;
                }
                pattern[3 * beat].kick = true;
                pattern[3 * beat].velocity = cg.kick_secondary_velocity;
            } else {
                for i in 0..beats_in_bar {
                    pattern[i * beat].kick = true;
                    pattern[i * beat].velocity = 0.9;
                }
            }
        }
    }

    // === SNARE PATTERNS ===
    // Backbeat

    if beats_in_bar == 4 {
        pattern[beat].snare = true;
        pattern[beat].velocity = cg.snare_backbeat_velocity;
        pattern[3 * beat].snare = true;
        pattern[3 * beat].velocity = cg.snare_backbeat_velocity;
    } else if beats_in_bar == 3 {
        pattern[2 * beat].snare = true;
        pattern[2 * beat].velocity = cg.snare_backbeat_velocity;
    } else if beats_in_bar == 2 {
        pattern[beat].snare = true;
        pattern[beat].velocity = cg.snare_backbeat_velocity;
    } else {
        let pos = (beats_in_bar - 1) * beat;
        pattern[pos].snare = true;
        pattern[pos].velocity = cg.snare_backbeat_velocity;
    }

    // Ghost notes selon tension
    if tension > cg.ghost_note_tension_threshold && sixteenth > 0 {
        let first_backbeat = if beats_in_bar == 3 { 2 * beat } else { beat };
        let ghost_pos = first_backbeat - sixteenth;
        if ghost_pos < steps {
            pattern[ghost_pos].snare = true;
            pattern[ghost_pos].velocity = cg.ghost_note_velocity;
        }
    }

    // === HI-HAT PATTERNS ===
    // Pattern régulier en croches ou doubles-croches selon density

    let hdt = &cg.hat_density_thresholds;
    if density < hdt[0] {
        // Sparse: juste les off-beats
        for i in 0..beats_in_bar {
            let pos = i * beat + eighth;
            if pos < steps && !pattern[pos].kick && !pattern[pos].snare {
                pattern[pos].hat = true;
                pattern[pos].velocity = cg.hat_sparse_velocity;
            }
        }
    } else if density < hdt[1] {
        // Standard: croches régulières
        for i in 0..(beats_in_bar * 2) {
            let pos = i * eighth;
            if pos < steps {
                let on_beat = i % 2 == 0;
                if pattern[pos].kick || (pattern[pos].snare && pattern[pos].velocity > 0.5) {
                    continue;
                }
                pattern[pos].hat = true;
                pattern[pos].velocity =
                    if on_beat { cg.hat_on_beat_velocity } else { cg.hat_off_beat_velocity };
            }
        }
    } else {
        // Dense: doubles-croches avec accents
        if sixteenth > 0 {
            for i in 0..(beats_in_bar * 4) {
                let pos = i * sixteenth;
                if pos >= steps {
                    break;
                }

                if pattern[pos].kick || (pattern[pos].snare && pattern[pos].velocity > 0.5) {
                    continue;
                }

                pattern[pos].hat = true;

                let is_eighth = i % 4 == 0;
                let is_sixteenth_and = i % 2 == 0;
                pattern[pos].velocity = if is_eighth {
                    cg.hat_dense_on_velocity
                } else if is_sixteenth_and {
                    cg.hat_dense_off_velocity
                } else {
                    cg.hat_dense_ghost_velocity
                };
            }
        }
    }

    // === BASS PATTERNS ===
    for i in 0..steps {
        if pattern[i].kick {
            pattern[i].bass = true;
        } else if tension > cg.bass_split_tension_threshold
            && i >= sixteenth
            && pattern[i - sixteenth].kick
        {
            pattern[i].bass = true;
        }
    }

    // === LEAD PATTERNS ===
    // Snap melody interval to beat-aligned subdivisions (beat, eighth, sixteenth)
    // so lead triggers produce clean note durations on the sheet.
    let melody_interval = {
        let max_interval = beat.max(1);
        let min_interval = eighth.max(1);
        #[allow(clippy::cast_possible_truncation, clippy::cast_sign_loss)]
        let raw = (max_interval as f32 * (1.0 - density) + min_interval as f32 * density).round()
            as usize;
        // Snap to nearest beat-aligned interval
        [beat, eighth, sixteenth.max(1)]
            .into_iter()
            .filter(|&i| i > 0)
            .min_by_key(|&i| (i as isize - raw as isize).unsigned_abs())
            .unwrap_or(beat.max(1))
    };
    for i in (0..steps).step_by(melody_interval) {
        let jitter = if tension > 0.5 && sixteenth > 0 { sixteenth } else { 0 };
        let final_pos = (i + jitter) % steps;
        pattern[final_pos].lead = true;
    }

    pattern
}

