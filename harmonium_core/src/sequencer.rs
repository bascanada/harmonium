    /// Accent pattern [1.0, 0.5] produces alternating velocities.
    #[test]
    fn test_polygon_alternating_accent_pattern() {
        let accent = AccentPattern::from_slice(&[1.0, 0.5]);
        let poly = Polygon::with_accents(4, 0, 0.8, accent);

        // 16-step cycle, square polygon hits at 0, 4, 8, 12
        let total_steps = 16;
        let mut velocities = Vec::new();
        for step in 0..total_steps {
            let (hit, vi) = poly.hit_info(step, total_steps);
            if hit {
                velocities.push(poly.velocity_at(vi));
            }
        }

        assert_eq!(velocities.len(), 4, "square in 16 steps should hit 4 times");
        // vertex 0 -> accent 1.0 -> 0.8*1.0=0.8
        // vertex 1 -> accent 0.5 -> 0.8*0.5=0.4
        // vertex 2 -> accent 1.0 -> 0.8*1.0=0.8
        // vertex 3 -> accent 0.5 -> 0.8*0.5=0.4
        let expected = [0.8, 0.4, 0.8, 0.4];
        for (i, (&got, &exp)) in velocities.iter().zip(expected.iter()).enumerate() {
            assert!(
                (got - exp).abs() < f32::EPSILON,
                "hit {i}: expected {exp}, got {got}"
            );
        }
    }

