        let poly = Polygon::with_accents(6, 0, 1.0, accent);

        let total_steps = 12; // 6 vertices in 12 steps, hits every 2 steps
        let mut velocities = Vec::new();
        for step in 0..total_steps {
            let (hit, vi) = poly.hit_info(step, total_steps);
            if hit {
                velocities.push((vi, poly.velocity_at(vi)));
            }
        }

